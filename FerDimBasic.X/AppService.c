void AppServiceInit(void)
{
    //Service Button
    DrvGpioInitPin(PORT_E, 1, PIN_IO_FUNC_INPUT);           //Service Button RE1
    
}
/*--------------------------------------------------------------------------------------------------------------------*/
void AppServiceHandler(void)
{
    if(!DrvGpioIsPinHigh(PORT_E, 1))        //Button Service mode is pressed
    {
        AppLEDControl(BUZZY, false, GREEN);
    }
}
/*--------------------------------------------------------------------------------------------------------------------*/
bool AppServiceGet(void)
{
    return !DrvGpioIsPinHigh(PORT_E, 1);            //Service button is active low
}
