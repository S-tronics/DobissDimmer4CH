/**
 * @brief   Initialise function for this module
 *
 *	Initialising this module
 *
 */
void StdEepromInit(void);

/**
 * 	@brief   Function to write to EepromData of the µC
 *
 *	@param	address	:	Addres of the Eeprom data
 *	@param	data	:	Data for the Eeprom
 *
 */
void StdEepromWrite(UNSIGNED_16 address, UNSIGNED_16 data);

/**
 * 	@brief   Function to read/write to EepromData of the µC
 *
 *	@param	rw	: 	READ or WRITE an array
 *  @param	address	:	Addres of the Eeprom data
 *	@param	*data	:	Pointer to the data array
 *	@param	depth	:	Length of the array to write to eeprom
 *
 */
void StdEepromRwArray(UNSIGNED_16 address, STD_EE_ACTION rw, UNSIGNED_16* data, UNSIGNED_8 depth);

/**
 * 	@brief   Function to write to EepromData of the µC
 *
 *	@param	address	:	Addres of the Eeprom data
 *	@return			:	Data from the Eeprom
 *
 */
UNSIGNED_16 StdEepromRead(UNSIGNED_16 address);

/**
 * 	@brief   Function to read an array from EepromData of the µC
 *
 *	@param	address	:	Addres of the Eeprom data
 *	@param	*data	:	Pointer to the data array
 *	@param	depth	:	Length of the array to read from eeprom
 *
 */
void StdEepromReadArray(UNSIGNED_16 address, UNSIGNED_16* data, UNSIGNED_8 depth);
